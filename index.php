respective button above.
</pre>
<br>
<br>
<br>

<h1>Drivers</h1>
<pre>
This table contains information about each Driver on the grid like their first and 
last names, team they're apart of, driver number, nationality, birthday and total points.
</pre>
<h1>Teams</h1>
<pre>
This table contains information about each Team on the grid such as team name, country 
where their base is located, year founded, manager and amount of championships won.
</pre>
<h1>Sponsors</h1>
<pre>
This is a small table that simply has the name, country and team of a sponsor.
</pre>
<h1>Circuits</h1>
<pre>
This will tell you about the name and country of a particular circuit. 
You will also find different statistics on a circuit like length and number of turns.
</pre>
<h1>Races</h1>
<pre>
Another small table which has the date and country of a particular race that took place.
</pre>
<h1>Results</h1>
<pre>
We then have a table which displays the results of these different races. The date of the 
race that was won, winning driver and team of that event, the starting and finishing grid 
positions of that winning driver as well as their fastest lap.
</pre>
<h1>Championships</h1>
<pre>
Finally, the championships table will show you each year a driver won the championship. 
It will display the driver name, points as well as the team they're apart of and the team's points.
</pre>

